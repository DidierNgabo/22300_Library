    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        try {
            Registry registry = LocateRegistry.getRegistry(2000);
            IClientService cl = (IClientService) registry.lookup("ClientService");
            int input = JOptionPane.showConfirmDialog(null,
                    "Do you want to delete this book category ?", "Select an Option...", JOptionPane.YES_NO_OPTION);
            // 0=yes, 1=no, 2=cancel
            System.out.println(input);
            if (input == 0) {
                boolean result = cl.delete(clientId);
                System.out.println(result ? "deleted successfully " : "failed to delete");
                JOptionPane.showMessageDialog(null, "client  deleted successfully");
            } else {
                JOptionPane.showMessageDialog(null, "operation cancelled");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        table();
    }//GEN-LAST:event_deletebtnActionPerformed


