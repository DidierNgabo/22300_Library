    public void table() {
        Session s = util.Connection.getSessionFactory().openSession();
        Transaction tr = s.beginTransaction();
        List<models.Clients> list = s.createQuery("from Clients").list();
        tr.commit();
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) table.getModel();
        model.getDataVector().removeAllElements();

        Object[] obj = new Object[7];

        for (models.Clients cl : list) {
            obj[0] = cl.getRegNo();
            obj[1] = cl.getFirstname();
            obj[2] = cl.getLastname();
            obj[3] = cl.getPhone();
            obj[4] = cl.getEmail();
            ImageIcon image = new ImageIcon(new ImageIcon(cl.getMyImage()).getImage()
                    .getScaledInstance(150, 120, Image.SCALE_SMOOTH));
            obj[5] = image;
            obj[6] = cl.getClientCategory();

            model.addRow(obj);
            System.out.println("row is displayed");

        }
        s.close();

    }
