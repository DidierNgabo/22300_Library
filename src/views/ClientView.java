    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setDialogTitle("choose image");
        //filter ext
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.setFileFilter(filter);
        int result = file.showSaveDialog(null);
        //if user click on save in Jchoosed
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            path = selectedFile.getAbsolutePath();
            ImageIcon img = new ImageIcon(path);
            Image i = img.getImage();
            Image image = i.getScaledInstance(imglabel.getWidth(), imglabel.getHeight(), Image.SCALE_AREA_AVERAGING);
            imglabel.setIcon(new ImageIcon(image));
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("No File Selected");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    public void displayImg() {
        Session s = util.Connection.getSessionFactory().openSession();
        Transaction tr = s.beginTransaction();
        models.Clients imgNew = (models.Clients) s.get(models.Clients.class, clientId);
        byte[] bAvatar = imgNew.getPhoto();

        try {
            FileOutputStream fos = new FileOutputStream(path);
            fos.write(bAvatar);
            imglabel.setIcon(new ImageIcon(bAvatar));
            fos.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        File file = new File(path);
        byte[] imageData = new byte[(int) file.length()];

        try {
            FileInputStream fileInputStream = new FileInputStream(file);
            fileInputStream.read(imageData);
            fileInputStream.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        models.Clients client = new models.Clients();

        client.setFirstname(txtfirstname.getText());
        client.setLastname(txtlastname.getText());
        client.setEmail(txtemail.getText());
        client.setPhone(txtphone.getText());
        client.setPhoto(imageData);
        client.setClientCategory(catcombo.getSelectedItem().toString());
        try {
            Registry registry = LocateRegistry.getRegistry(2000);
            IClientService cl = (IClientService) registry.lookup("ClientService");
            boolean result = cl.insert(client);
            System.out.println(result ? "sucess" : "fail");
            if (result) {
                JOptionPane.showMessageDialog(null, "client successfully saved");
            } else {
                JOptionPane.showMessageDialog(null, "client failed to  saved", "error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        table();
    }//GEN-LAST:event_saveBtnActionPerformed

